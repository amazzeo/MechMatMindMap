      <font size="4">The study of structural mechanics and materials allows us to predict with reasonable accuracy how much a structure will deflect and its safety against catastrophic failure. Indeed, it is a revolutionary concept to test a small sample of material to characterize its elasticity and strength that we can then relate to anticipated levels of stress and strain within an engineered structure. As our ability to characterize materials and predict invisible distributions of stress within structures has improved, our confidence in the predictions and accepted factors have decreased by an order of magnitude.</font>
    </p>
