  <body>
    <p>
      We aim to become a living source of content for undergraduate courses in mechanics of materials for independent study and meaningful learning sessions with peers and expert instructors. We invite instructors and students to work together with crowd-sourced content freely available on GitHub and Wikmedia sites using open-source platforms. We hope to create, assemble, and curate high-quality content, examples, problems, and videos to allow university-level mastery in an engaging format.
    </p>
    <p>
      The hope is that students and instructors will use the material curated and created for the teaching and studying of Mechanics of Materials. This material is to provide living content for an undergraduate course in mechanics of materials or strength of materials. The material here will eventually be of sufficient quality and interest for self-learning or prescribed study by instructors/faculty members. One objective is that the material here would facilitate in-class discussions, group projects, or problem-solving that would leverage the instructors' expertise to enhance learning outcomes. Another objective is to facilitate students, instructors, professionals, and interested users adding/curating content to accommodate learning styles that might benefit from a spectrum of insights coming from learners and teachers with diverse backgrounds.
    </p>
