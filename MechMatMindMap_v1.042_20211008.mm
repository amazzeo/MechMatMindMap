      <font size="4">What if there were a set of characteristics associated with solids that could help us determine whether a material will break? Well, these material properties exist and have standardized units and defined metrics for their characterization. These parameters also apply across multiples scales of length. In fact, the material properties go beyond only determining the likelihood of failure, yielding, or breaking, but they also help us determine how much deflection/displacement we should expect at a range of scales. </font>
    </p>
    <p style="margin-top: 0; margin-bottom: 0; margin-right: 0px; margin-left: 0px; color: rgb(32, 33, 34); font-family: sans-serif; font-size: 14px; font-style: normal; font-weight: 400; letter-spacing: normal; text-align: start; text-indent: 0px; text-transform: none; white-space: normal; word-spacing: 0px; background-color: rgb(255, 255, 255)">
      
